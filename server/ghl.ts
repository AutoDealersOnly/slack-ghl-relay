import { Router, Request, Response } from "express";

const GHL_API_KEY = "pit-4ceff49d-22c5-42df-bc34-8fb8a6a29fe2";
const GHL_LOCATION_ID = "UGJmliC4GETAgeO6IDXa";
const SLACK_BOT_TOKEN = "xoxb-414532258742-11486505580721-34wVikPoiBR6MR8ZIRLb8hOr";

export const ghlRouter = Router();

ghlRouter.post("/ghl", async (req: Request, res: Response) => {
  const body = req.body as Record<string, string>;
  const channelId = body.channel_id;
  const channelName = body.channel_name;

  if (!channelId || !channelName) {
    res.status(400).send("Missing channel_id or channel_name");
    return;
  }

  // Acknowledge Slack immediately (must respond within 3s)
  res.status(200).send("");

  try {
    // Step 1: Join the channel
    await fetch("https://slack.com/api/conversations.join", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${SLACK_BOT_TOKEN}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({ channel: channelId }),
    });

    // Step 2: Query GHL for the production record matching the channel name
    const ghlResp = await fetch(
      "https://services.leadconnectorhq.com/objects/custom_objects.production/records/search",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${GHL_API_KEY}`,
          Version: "v3",
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          locationId: GHL_LOCATION_ID,
          page: 1,
          pageLimit: 1,
          query: channelName,
        }),
      }
    );

    const ghlData = (await ghlResp.json()) as {
      records?: Array<{ properties: Record<string, unknown> }>;
    };

    const record = ghlData.records?.[0];

    let markdown: string;

    if (!record) {
      markdown = `# GHL Production Details\n\n> No production record found for channel \`${channelName}\`.\n\n*Generated by /ghl*`;
    } else {
      const p = record.properties as Record<string, unknown>;
      const price =
        p.price && typeof p.price === "object"
          ? String((p.price as Record<string, unknown>).value ?? "")
          : String(p.price ?? "");

      markdown = `# GHL Production Details

## Campaign Info

| Field | Value |
|---|---|
| **Production** | ${p.production ?? ""} |
| **Dealership** | ${p.dealership ?? ""} |
| **Event Start** | ${p.event_start ?? ""} |
| **Event End** | ${p.event_end ?? ""} |
| **SCF Date** | ${p.scf_date ?? ""} |
| **Deal Stage** | ${p.deal_stage ?? ""} |

## Production Details

| Field | Value |
|---|---|
| **Mailer** | ${p.mailer_2 ?? ""} |
| **Mail Count** | ${p.mail_count ?? ""} |
| **Job #** | ${p.job_numbers ?? ""} |
| **Price** | ${price} |

## Team

| Field | Value |
|---|---|
| **Sales Rep** | ${p.sales_rep ?? ""} |
| **Closer** | ${p.closer ?? ""} |
| **Greeter** | ${p.greeter ?? ""} |

---
*Generated by /ghl*`;
    }

    // Step 3: Create the canvas in the channel
    const canvasResp = await fetch("https://slack.com/api/canvases.create", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${SLACK_BOT_TOKEN}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        channel_id: channelId,
        document_content: {
          type: "markdown",
          markdown,
        },
      }),
    });

    const canvasData = (await canvasResp.json()) as { ok: boolean; error?: string; canvas_id?: string };

    if (canvasData.ok) {
      console.log(`[ghl] Canvas created: ${canvasData.canvas_id} in channel ${channelId}`);
    } else {
      console.error(`[ghl] Canvas creation failed: ${canvasData.error}`);
      // Send an error message to the channel so the user knows
      await fetch("https://slack.com/api/chat.postMessage", {
        method: "POST",
        headers: {
          Authorization: `Bearer ${SLACK_BOT_TOKEN}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          channel: channelId,
          text: `⚠️ Could not create GHL canvas: ${canvasData.error}`,
        }),
      });
    }
  } catch (err) {
    console.error("[ghl] Error:", err);
  }
});
